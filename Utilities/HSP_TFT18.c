static uint8_t hsp_spi_write_byte(uint8_t byte)
{
    while(RESET == (SPI_STAT(TFT18_SPI_BASEADDR)&SPI_FLAG_TBE));
    SPI_DATA(TFT18_SPI_BASEADDR) = byte;

    while(RESET == (SPI_STAT(TFT18_SPI_BASEADDR)&SPI_FLAG_RBNE));
    return(SPI_DATA(TFT18_SPI_BASEADDR));
} 

void hsp_tft18_write_cmd(uint8_t dat)
{
	LCD_DC_LOW();
    hsp_spi_write_byte(dat);
}

void hsp_tft18_write_byte(uint8_t dat)
{
	LCD_DC_HIGH();
    hsp_spi_write_byte(dat);
}

void hsp_tft18_write_2byte(uint16_t dat)
{
	LCD_DC_HIGH();
    hsp_spi_write_byte(dat>>8);
    hsp_spi_write_byte(dat);
}

