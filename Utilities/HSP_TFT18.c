void hsp_tft18_draw_frame(uint8_t x_start, uint8_t y_start, uint8_t dx, uint8_t dy, uint16_t color)
{
    if(((x_start+dx)>TFT_X_MAX) || ((y_start+dy)>TFT_Y_MAX) || \
        (x_start>(TFT_X_MAX-1)) || (y_start>(TFT_Y_MAX-1)))
		return;

    hsp_tft18_draw_line_h(x_start, y_start, dx, color);
	hsp_tft18_draw_line_h(x_start, y_start+dy, dx, color);
    hsp_tft18_draw_line_v(x_start, y_start, dy, color);
	hsp_tft18_draw_line_v(x_start+dx, y_start, dy, color);
}

void hsp_tft18_draw_block(uint8_t x_start, uint8_t y_start, uint8_t dx, uint8_t dy, uint16_t color)
{
    uint8_t i, j;
    
    if(((x_start+dx)>TFT_X_MAX) || ((y_start+dy)>TFT_Y_MAX) || \
        (x_start>(TFT_X_MAX-1)) || (y_start>(TFT_Y_MAX-1)))
		return;

    for(i=0; i<dy; i++)
    {
        for(j=0; j<dx; j++)
        {
            hsp_tft18_set_xy(x_start+j, y_start+i);
            hsp_tft18_write_2byte(color);
        }
    }
}

