	hsp_tft18_write_byte(0x3C);
	hsp_tft18_write_byte(0x3C);

    hsp_tft18_write_cmd(0xB4);		// Display Inversion Control
	hsp_tft18_write_byte(0x03);		// Frame inversion

	hsp_tft18_write_cmd(0x3A);		// Interface pixel format
    hsp_tft18_write_byte(0x05);		// Interface format: 16-bit pixel; use "0x55" when writing 16-bit data

    hsp_tft18_write_cmd(0x20);		// Display inversion off
    hsp_tft18_write_cmd(0x13);		// Partial mode off

    hsp_tft18_write_cmd(0x26);		// Gamma curve select   		  	
    hsp_tft18_write_byte(0x01);		// 1,2,4,8
    
    hsp_tft18_write_cmd(0x29);		// Display on
	
    hsp_tft18_write_cmd(0x36);		// Memory Data Access Control: MX, MY, RGB mode 
